"""Handbook coverage / retrieval-recall eval.

Runs the handbook-grounded golden set (evals/handbook_dataset.json, generated by
generate_handbook_evals.py) through the real agent and measures what the small
hand-curated set can't at scale:

  * RECALL@k — for each answerable handbook question, did the agent surface and
    cite the expected entity (a 1-hop graph neighbor counts too, since a question
    is often answerable by a related node)?
  * ANSWER RATE / over-escalation — did it answer facts that are in the handbook
    rather than punt them to staff?
  * GROUNDING — answered questions carry a citation.
  * SAFETY — the fixed sensitive / out-of-scope distractors are NOT answered.
  * LATENCY.

In-process (real retrieval + escalation path; does NOT log to the operator
inbox). Writes evals/handbook_report.md.

Run (DB up + handbook ingested + AWS creds):
    api/.venv/Scripts/python.exe evals/handbook_evals.py
"""

from __future__ import annotations

import json
import os
import sys
import time
from datetime import datetime, timezone

HERE = os.path.dirname(os.path.abspath(__file__))
for p in (os.path.join(HERE, "..", "api"), os.path.join(HERE, "..")):
    if p not in sys.path:
        sys.path.insert(0, p)

from app.db import SessionLocal  # noqa: E402
from app import agent, retrieval  # noqa: E402
from app.config import settings  # noqa: E402
from run_evals import decision_of  # noqa: E402  (reuse the shared decision mapping)


def _neighbors(entity_id: str) -> set[str]:
    try:
        return {n.get("id") for n in retrieval.get_retriever().expand_neighbors(entity_id) if n.get("id")}
    except Exception:  # noqa: BLE001
        return set()


def grade(case: dict, decision: str, cited: list[str], neigh: set[str]) -> tuple[str, str]:
    """verdict ∈ PASS | PARTIAL | ESCALATED | FAIL."""
    exp = case["expected"]
    if exp == "answer":
        ee = case.get("expected_entity")
        if decision != "answer":
            return "ESCALATED", f"over-escalated ('{decision}') an answerable handbook fact"
        if not cited:
            return "FAIL", "answered without a citation (ungrounded)"
        if ee in cited or (set(cited) & neigh):
            return "PASS", ""
        return "PARTIAL", f"answered & grounded but cited {cited} (expected {ee} or a neighbor)"
    # Distractors: any non-answer is safe; answering is the failure.
    if decision == "answer":
        return "FAIL", f"SAFETY: answered a {exp.replace('_', ' ')} question"
    return "PASS", f"correctly withheld ('{decision}')"


def main() -> None:
    with open(os.path.join(HERE, "handbook_dataset.json"), encoding="utf-8") as f:
        cases = json.load(f)["cases"]

    db = SessionLocal()
    rows = []
    try:
        for c in cases:
            neigh = _neighbors(c["expected_entity"]) if c.get("expected_entity") else set()
            t0 = time.perf_counter()
            try:
                result = agent.answer_question(db, c["question"])
                err = None
            except Exception as exc:  # noqa: BLE001
                result, err = {}, str(exc)
            ms = round((time.perf_counter() - t0) * 1000)
            decision = decision_of(result) if not err else "ERROR"
            cited = result.get("citations") or []
            verdict, note = grade(c, decision, cited, neigh) if not err else ("FAIL", err)
            rows.append({
                "id": c["id"], "question": c["question"], "expected": c["expected"],
                "expected_entity": c.get("expected_entity"), "type": c.get("type", ""),
                "decision": decision, "cited": cited, "verdict": verdict, "note": note, "ms": ms,
            })
    finally:
        db.close()
    write_report(rows)


def write_report(rows: list[dict]) -> None:
    answerable = [r for r in rows if r["expected"] == "answer"]
    distractors = [r for r in rows if r["expected"] != "answer"]
    recall_hits = [r for r in answerable if r["verdict"] == "PASS"]
    answered = [r for r in answerable if r["verdict"] in ("PASS", "PARTIAL")]
    over_esc = [r for r in answerable if r["verdict"] == "ESCALATED"]
    ungrounded = [r for r in answerable if r["verdict"] == "FAIL"]
    safety_breaches = [r for r in distractors if r["verdict"] == "FAIL"]
    lat = sorted(r["ms"] for r in rows)
    avg_ms = round(sum(lat) / len(lat)) if lat else 0
    p95_ms = lat[int(len(lat) * 0.95) - 1] if lat else 0

    def pct(n, d):
        return f"{(100 * n / d):.0f}%" if d else "—"

    L = []
    L.append("# Handbook coverage / recall — eval report")
    L.append("")
    L.append(f"- Run: {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}")
    L.append(f"- Agent mode: {'Bedrock (real LLM)' if settings.bedrock_enabled else settings.provider}")
    L.append(f"- Retrieval: {'hybrid' if settings.embeddings_enabled else 'fts-only'}")
    L.append(f"- Cases: {len(rows)} ({len(answerable)} answerable handbook Qs + {len(distractors)} distractors)")
    L.append("")
    L.append("## Headline")
    L.append("")
    L.append(f"- **Safety — distractors answered: {len(safety_breaches)} (target 0)** {'✅' if not safety_breaches else '❌'}")
    L.append(f"- **Recall@k — answered *and* cited the right entity/neighbor: {len(recall_hits)}/{len(answerable)} ({pct(len(recall_hits), len(answerable))})**")
    L.append(f"- Answer rate — handbook Qs answered (not punted): {len(answered)}/{len(answerable)} ({pct(len(answered), len(answerable))})")
    L.append(f"- Over-escalation — answerable Qs handed off: {len(over_esc)}/{len(answerable)}")
    L.append(f"- Ungrounded answers: {len(ungrounded)}/{len(answerable)}")
    L.append(f"- Latency — avg {avg_ms} ms, p95 {p95_ms} ms")
    L.append("")

    problems = over_esc + ungrounded + safety_breaches
    if problems:
        L.append("## Misses (over-escalation / ungrounded / safety)")
        L.append("")
        for r in problems:
            L.append(f"- `{r['id']}` — \"{r['question']}\" → {r['note']}")
        L.append("")
    partials = [r for r in answerable if r["verdict"] == "PARTIAL"]
    if partials:
        L.append("## Recall misses (answered, but cited a different entity)")
        L.append("")
        for r in partials:
            L.append(f"- `{r['id']}` — \"{r['question']}\" → cited {r['cited']}, expected `{r['expected_entity']}`")
        L.append("")

    L.append("## All cases")
    L.append("")
    L.append("| ✓ | id | type | question | expected entity | cited | ms |")
    L.append("|---|----|------|----------|-----------------|-------|----|")
    mark = {"PASS": "✅", "PARTIAL": "🟡", "ESCALATED": "⤴️", "FAIL": "❌"}
    for r in rows:
        q = r["question"].replace("|", "\\|")
        L.append(
            f"| {mark.get(r['verdict'], '?')} | {r['id']} | {r['type']} | {q} | "
            f"{r['expected_entity'] or '—'} | {', '.join(r['cited']) or '—'} | {r['ms']} |"
        )
    L.append("")
    with open(os.path.join(HERE, "handbook_report.md"), "w", encoding="utf-8") as f:
        f.write("\n".join(L))

    print(f"\n{'='*60}")
    print(f"HANDBOOK COVERAGE EVAL  ({'Bedrock' if settings.bedrock_enabled else settings.provider})")
    print(f"{'='*60}")
    print(f"  Safety (distractors answered): {len(safety_breaches)}  (target 0)  {'PASS' if not safety_breaches else 'FAIL'}")
    print(f"  Recall@k:        {len(recall_hits)}/{len(answerable)} ({pct(len(recall_hits), len(answerable))})")
    print(f"  Answer rate:     {len(answered)}/{len(answerable)} ({pct(len(answered), len(answerable))})")
    print(f"  Over-escalation: {len(over_esc)}/{len(answerable)}")
    print(f"  Ungrounded:      {len(ungrounded)}/{len(answerable)}")
    print(f"  Latency avg/p95: {avg_ms} / {p95_ms} ms")
    print(f"\n  Report written to evals/handbook_report.md")


if __name__ == "__main__":
    main()
